package model;

public class EntryBean {
	private int sub_id;
	private String sub_name;
	private int sc_id;
	/**
	 * sub_idを取得します。
	 * @return sub_id
	 */
	public int getSub_id() {
	    return sub_id;
	}
	/**
	 * sub_idを設定します。
	 * @param sub_id sub_id
	 */
	public void setSub_id(int sub_id) {
	    this.sub_id = sub_id;
	}
	/**
	 * sub_nameを取得します。
	 * @return sub_name
	 */
	public String getSub_name() {
	    return sub_name;
	}
	/**
	 * sub_nameを設定します。
	 * @param sub_name sub_name
	 */
	public void setSub_name(String sub_name) {
	    this.sub_name = sub_name;
	}
	/**
	 * sc_idを取得します。
	 * @return sc_id
	 */
	public int getSc_id() {
	    return sc_id;
	}
	/**
	 * sc_idを設定します。
	 * @param sc_id sc_id
	 */
	public void setSc_id(int sc_id) {
	    this.sc_id = sc_id;
	}
}
